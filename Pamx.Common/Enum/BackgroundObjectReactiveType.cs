namespace Pamx.Common.Enum;

/// <summary>
/// The type of music reaction of background object
/// </summary>
public enum BackgroundObjectReactiveType
{
    /// <summary>
    /// No reaction
    /// </summary>
    None,
    
    /// <summary>
    /// React to low frequencies
    /// </summary>
    Bass,
    
    /// <summary>
    /// React to mid frequencies
    /// </summary>
    Mid,
    
    /// <summary>
    /// React to high frequencies
    /// </summary>
    Treble,
}