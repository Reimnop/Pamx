namespace Pamx.Common.Enum;

/// <summary>
/// The easing functions for keyframes
/// </summary>
public enum Ease
{
    /// <summary>
    /// Linear easing function
    /// </summary>
    Linear,
    
    /// <summary>
    /// Instant easing function
    /// </summary>
    Instant,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates
    /// </summary>
    InSine,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates
    /// </summary>
    OutSine,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle
    /// </summary>
    InOutSine,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with an elastic effect
    /// </summary>
    InElastic,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with an elastic effect
    /// </summary>
    OutElastic,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with an elastic effect
    /// </summary>
    InOutElastic,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with a backtracking effect
    /// </summary>
    InBack,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with a backtracking effect
    /// </summary>
    OutBack,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with a backtracking effect
    /// </summary>
    InOutBack,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with a bouncing effect
    /// </summary>
    InBounce,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with a bouncing effect
    /// </summary>
    OutBounce,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with a bouncing effect
    /// </summary>
    InOutBounce,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with a quadratic effect
    /// </summary>
    InQuad,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with a quadratic effect
    /// </summary>
    OutQuad,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with a quadratic effect
    /// </summary>
    InOutQuad,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with a circular effect
    /// </summary>
    InCirc,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with a circular effect
    /// </summary>
    OutCirc,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with a circular effect
    /// </summary>
    InOutCirc,
    
    /// <summary>
    /// Easing function that starts slowly and accelerates with an exponential effect
    /// </summary>
    InExpo,
    
    /// <summary>
    /// Easing function that starts quickly and decelerates with an exponential effect
    /// </summary>
    OutExpo,
    
    /// <summary>
    /// Easing function that starts and ends slowly, but accelerates in the middle with an exponential effect
    /// </summary>
    InOutExpo,
}